# print a message
print("hello Python learning")

# new line character \n
print("hello python! \n Have a great day")

# use plus to concatenate strings
name = "siva" + "karthik"
print(name)

# input function and fstring
designation = input("please enter your designation: ")
print(f"Designation: {designation}")

# len() to print the length of a string
name = "sivakarthik"
print(len(name))

# len() to print the length of an array/list
names = ["siva", "karthik", "vaishu", "aara"]
print(len(names))

# variables in Python follow snake case
long_name = "Siva Karthik Narisetty"
print(long_name)

# implicit continuation in string
name = ("siva"
        "karthik")
print(f"{name} is implicit continuation string")




